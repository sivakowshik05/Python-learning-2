# ternary operator

# condition_if_true if condition else condition_if_false
# for True condition
is_friend1 = True
can_message1 = "message allowed" if is_friend1 else "not allowed to message"
print(can_message1)

# for false condition
is_friend2 = False
can_message2 = "message allowed" if is_friend2 else "not allowed to message"
print(can_message2)
